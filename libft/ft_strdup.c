#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	int		i;
	int		l;

	l = 0;
	while (src[l])
		++l;
	if (!(dup = malloc(sizeof(char) * (l + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
